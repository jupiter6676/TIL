N = int(input())
dp = [[0] * 10 for _ in range(N + 1)]   # i: 자릿수, j: j로 시작하는 수

for i in range(1, 10):
    dp[1][i] = 1

for i in range(2, N + 1):
    for j in range(10):
        # ex) 총 3자리 수인데, 맨 앞이 0 → = 2자리 수, 맨 앞이 1
        if j == 0:
            dp[i][j] = dp[i - 1][1]

        elif j == 9:
            dp[i][j] = dp[i - 1][8]

        else:
            dp[i][j] = dp[i - 1][j - 1] + dp[i - 1][j + 1]

# print(dp)
print(sum(dp[N]) % 1000000000)


